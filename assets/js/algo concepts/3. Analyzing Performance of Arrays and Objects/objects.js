/*

Objects :- They are unordered DS and everything is stored in key value pairs.
    - Use Object when :-
        - we don't need order.
        - we need fast access / insertion and removal

Object Operations and their BIG O notation :- 
    - Insertion :- O(1)
    - Removal :- O(1)
    - Searching :- O(N)         // Checking for a particular value in the Object
    - Access :- O(1)

Object Methods :-
    - Object.keys() :- O(N)
    - Object.values() :- O(N)
    - Object.entries() :- O(N)
    - Object.hasOwnProperty() :- O(1)

*/